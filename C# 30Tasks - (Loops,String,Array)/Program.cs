using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace C__30Tasks____Loops_String_Array_
{
    internal class Program
    {
        static void Main(string[] args)
        {

            //1️ ZigZag Numbers
            int num = 5;
            for (int i = 1; i <= num; i++)
            {
                for (int j = num; j >= i; j++)
                {
                    Console.Write(i);
                }

            }
















        }
    }
}
